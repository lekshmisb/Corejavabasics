package d;

public class ArithmeticOperation 
{
	public static void main(String[] args) 
	{
		
		ArithmeticOperation a=new ArithmeticOperation();
		a.addNumber();
		a.subNumber();
		a.mulNumber();
		a.divNumber();
		
		
	}
		
		void addNumber()
		{
		
		int a=10;
		int b=20;
		System.out.println("The result is :"+(a+b));
		
		
		}
		void subNumber()
		{
		
		int a=10;
		int b=20;
		System.out.println("The result is :"+(a-b));
		
		
		}
		void mulNumber()
		{
		
		int a=10;
		int b=20;
		System.out.println("The result is :"+(a*b));
		
		
		}
		void divNumber()
		{
		
		int a=10;
		int b=20;
		System.out.println("The result is :"+(a/b));
		
		
		}
		
		

}
